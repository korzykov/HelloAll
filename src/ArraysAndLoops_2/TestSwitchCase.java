package ArraysAndLoops_2;

public class TestSwitchCase {
    public static void main(String[] args) {
        int num = 2;
        switch (num) {
            case 1 -> System.out.println("Number is 1!");
            case 8 -> System.out.println("Number is 8!");
            case 9 -> System.out.println("Number is 9!");
            default -> System.out.println("Number is not 1, 8 or 9");
        }

    }
}
